    <dt>Que es el I.V.A </dt>  La sigla IVA (impuesto de venta agregado) hace referencia a un tributo o impuesto que deben pagar los consumidores al estado por el uso de un determinado servicio o la adquisición de un bien. 
					Y se trata de una tasa que se calcula sobre el consumo de los productos, los servicios, las transacciones comerciales y las importaciones
    </div>
    <div class= "container mt-5 col-9">
       <dt> ¿Como Funciona? </dt>  A diferencia de los impuestos directos, no repercute directamente sobre los ingresos, por el contrario, recae sobre los costos de producción y venta de las empresas y se devenga de los precios que los consumidores pagan por dichos productos.
					Esto significa que se aplica sobre el consumo y que resulta financiado por el consumidor final. 
					El IVA se calcula de la siguiente manera. Un empresario vende u ofrece un servicio y factura el costo del mismo, devengando el impuesto repercutido (el cual debe calcularse teniendo en cuenta la legislación vigente donde se especifica a cuánto corresponde el mismo en base al tipo de producto o servicio ofertado). 
Cada dos o tres meses, según lo estipulado por la ley para el rubro en el que se 
encuentra registrado, el empresario deberá realizar la declaración de impuestos, donde se sumarán las cuotas que corresponden al IVA de ese período. Allí se realiza el cálculo de lo pagado y lo recibido en referencia a este impuesto y se establece el saldo. Si éste es negativo (cuando las cuotas del IVA soportado han sido superiores a las repercutidas) el empresario podrá compensar
 futuros saldos o solicitar la devolución de dichas tasas. Si es positivo, deberá realizar el pago de las mismas.

